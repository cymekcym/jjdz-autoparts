package javatar.model;

public class LinkedInUser {

    private String firstName;

    private String lastName;

    private String headline;

    private String id;


    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getHeadline() {
        return headline;
    }

    public String getId() {
        return id;
    }


}
